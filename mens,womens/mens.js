function handlebrand() {
    var select = document.querySelector("#sortbrand").value

    var filt = productsdata.filter(function (elem) {
        return elem.brand == select
    })

    display(filt)
}

// sort by rate----->

function handlerate() {
    var select = document.querySelector("#sortrate").value

    if (select == "none") {

        display(productsdata)
    }
    else if (select == "LTH") {
        var filt = productsdata.sort(function (a, b) {
            return a.rate - b.rate
        })
        display(filt)
    }
    else if (select == "HTL") {
        var filt = productsdata.sort(function (a, b) {
            return b.rate - a.rate
        })
        display(filt)
    }

}



